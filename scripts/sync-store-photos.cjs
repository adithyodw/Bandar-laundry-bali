/**
 * Copies images from `Photos of actual stores/` → `public/stores/`
 * (filenames preserved as-is) and regenerates `lib/store-gallery.synced.ts`.
 *
 * Run:  node scripts/sync-store-photos.cjs
 * Auto: runs before every `next build` via "prebuild" npm script.
 */
/* eslint-disable @typescript-eslint/no-require-imports */
const fs   = require("fs");
const path = require("path");

const root    = path.join(__dirname, "..");
const srcDir  = path.join(root, "Photos of actual stores");
const destDir = path.join(root, "public", "stores");
const outFile = path.join(root, "lib", "store-gallery.synced.ts");

const extOk = new Set([".jpg", ".jpeg", ".png", ".webp"]);

const BRAND = "Bandar Laundry Express";

function writeEmpty() {
  fs.writeFileSync(
    outFile,
    `/* Generated by scripts/sync-store-photos.cjs — do not edit by hand */\nimport type { StoreGalleryImage } from "./store-gallery.types";\n\nexport const SYNCED_STORE_IMAGES: StoreGalleryImage[] = [];\n`,
    "utf8",
  );
}

function pickDest(used, name, ext) {
  let candidate = name + ext;
  let n = 2;
  while (used.has(candidate)) { candidate = `${name}-${n}${ext}`; n++; }
  used.add(candidate);
  return candidate;
}

/**
 * Derive a human-readable, bilingual caption from the raw filename stem.
 *   "10"            → { id: "Bandar Laundry Express",        en: "Bandar Laundry Express" }
 *   "kebo-iwa"      → { id: "Kebo Iwa — Bandar Laundry…",   en: "Kebo Iwa — Bandar…" }
 *   "front-store-1" → { id: "Front Store 1 — Bandar…",       en: "Front Store 1 — Bandar…" }
 */
function deriveCaption(rawStem) {
  const clean = rawStem.replace(/[-_]+/g, " ").replace(/\s+/g, " ").trim();
  const isNumericOnly = /^\d+$/.test(rawStem.trim());

  if (isNumericOnly || clean === "") {
    return { id: BRAND, en: BRAND };
  }

  // Title-case
  const titled = clean.replace(/\b\w/g, (c) => c.toUpperCase());
  return {
    id: `${titled} — ${BRAND}`,
    en: `${titled} — ${BRAND}`,
  };
}

function main() {
  if (!fs.existsSync(srcDir)) {
    console.log('Folder "Photos of actual stores" not found — gallery will be empty.');
    writeEmpty();
    return;
  }

  fs.mkdirSync(destDir, { recursive: true });

  const files = fs
    .readdirSync(srcDir)
    .filter((f) => extOk.has(path.extname(f).toLowerCase()))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" }));

  if (files.length === 0) {
    console.log('No images found in "Photos of actual stores" — gallery will be empty.');
    writeEmpty();
    return;
  }

  const used  = new Set();
  const items = [];

  for (const f of files) {
    const extFull = path.extname(f);
    const ext     = extFull.toLowerCase();
    const rawStem = path.basename(f, extFull);
    const safeStem = rawStem.replace(/[^a-zA-Z0-9-_.]+/g, "-").replace(/^-+|-+$/g, "").slice(0, 80) || "photo";
    const destName = pickDest(used, safeStem, ext);

    fs.copyFileSync(path.join(srcDir, f), path.join(destDir, destName));
    const caption = deriveCaption(rawStem);
    items.push({ src: `/stores/${destName}`, caption });
  }

  const entries = items.map(
    ({ src, caption }) =>
      `  { src: ${JSON.stringify(src)}, caption: { id: ${JSON.stringify(caption.id)}, en: ${JSON.stringify(caption.en)} } },`,
  );

  fs.writeFileSync(
    outFile,
    `/* Generated by scripts/sync-store-photos.cjs — do not edit by hand */\nimport type { StoreGalleryImage } from "./store-gallery.types";\n\nexport const SYNCED_STORE_IMAGES: StoreGalleryImage[] = [\n${entries.join("\n")}\n];\n`,
    "utf8",
  );
  console.log(`Synced ${items.length} photo(s) → public/stores/ and lib/store-gallery.synced.ts`);
}

main();
