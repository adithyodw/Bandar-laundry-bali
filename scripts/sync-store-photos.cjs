/**
 * Copies images from `Photos of actual stores/` → `public/stores/`
 * (preserving sanitized filenames) and regenerates `lib/store-gallery.synced.ts`.
 *
 * Run: node scripts/sync-store-photos.cjs
 */
/* eslint-disable @typescript-eslint/no-require-imports */
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const srcDir = path.join(root, "Photos of actual stores");
const destDir = path.join(root, "public", "stores");
const outFile = path.join(root, "lib", "store-gallery.synced.ts");

const extOk = new Set([".jpg", ".jpeg", ".png", ".webp"]);

function writeEmpty() {
  const empty = `/* Generated by scripts/sync-store-photos.cjs — do not edit by hand */
import type { StoreGalleryImage } from "./store-gallery.types";

export const SYNCED_STORE_IMAGES: StoreGalleryImage[] = [];
`;
  fs.writeFileSync(outFile, empty, "utf8");
}

function pickDestName(used, base, ext) {
  let b = base || "photo";
  let candidate = `${b}${ext}`;
  let n = 2;
  while (used.has(candidate)) {
    candidate = `${b}-${n}${ext}`;
    n += 1;
  }
  used.add(candidate);
  return candidate;
}

function main() {
  if (!fs.existsSync(srcDir)) {
    console.log(
      'No "Photos of actual stores" folder at project root — leaving synced gallery empty.',
    );
    writeEmpty();
    return;
  }

  fs.mkdirSync(destDir, { recursive: true });
  const files = fs
    .readdirSync(srcDir)
    .filter((f) => extOk.has(path.extname(f).toLowerCase()));

  if (files.length === 0) {
    console.log("No .jpg / .png / .webp files in Photos of actual stores.");
    writeEmpty();
    return;
  }

  const usedNames = new Set();
  const items = [];
  files.sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

  for (const f of files) {
    const extFull = path.extname(f);
    const ext = extFull.toLowerCase();
    const rawBase = path.basename(f, extFull);
    const safeBase = rawBase
      .replace(/[^a-zA-Z0-9-_.]+/g, "-")
      .replace(/^-+|-+$/g, "")
      .slice(0, 80);
    const destName = pickDestName(usedNames, safeBase || "photo", ext);
    fs.copyFileSync(path.join(srcDir, f), path.join(destDir, destName));
    const captionBase =
      rawBase.replace(/[-_]+/g, " ").replace(/\s+/g, " ").trim() ||
      destName.replace(ext, "");
    const isNumeric = /^\d+$/.test(rawBase.trim());
    const captionEn = isNumeric
      ? `Location ${rawBase.trim()}`
      : captionBase;
    const captionId = isNumeric
      ? `Lokasi ${rawBase.trim()}`
      : captionBase;
    items.push({
      src: `/stores/${destName}`,
      captionEn,
      captionId,
    });
  }

  const lines = items.map(
    (it) =>
      `  { src: ${JSON.stringify(it.src)}, caption: { id: ${JSON.stringify(it.captionId + " — Bandar Laundry Express")}, en: ${JSON.stringify(it.captionEn + " — Bandar Laundry Express")} } },`,
  );

  const ts = `/* Generated by scripts/sync-store-photos.cjs — do not edit by hand */
import type { StoreGalleryImage } from "./store-gallery.types";

export const SYNCED_STORE_IMAGES: StoreGalleryImage[] = [
${lines.join("\n")}
];
`;
  fs.writeFileSync(outFile, ts, "utf8");
  console.log(`Synced ${items.length} image(s) → public/stores and lib/store-gallery.synced.ts`);
}

main();
