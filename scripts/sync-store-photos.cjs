/**
 * Copies images from `Photos of actual stores/` → `public/stores/`
 * and regenerates `lib/store-gallery.synced.ts`.
 *
 * Run: node scripts/sync-store-photos.cjs
 */
/* eslint-disable @typescript-eslint/no-require-imports */
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const srcDir = path.join(root, "Photos of actual stores");
const destDir = path.join(root, "public", "stores");
const outFile = path.join(root, "lib", "store-gallery.synced.ts");

const extOk = new Set([".jpg", ".jpeg", ".png", ".webp"]);

function writeEmpty() {
  const empty = `/* Generated by scripts/sync-store-photos.cjs — do not edit by hand */
import type { StoreGalleryImage } from "./store-gallery.types";

export const SYNCED_STORE_IMAGES: StoreGalleryImage[] = [];
`;
  fs.writeFileSync(outFile, empty, "utf8");
}

function main() {
  if (!fs.existsSync(srcDir)) {
    console.log(
      'No "Photos of actual stores" folder at project root — leaving synced gallery empty.',
    );
    writeEmpty();
    return;
  }

  fs.mkdirSync(destDir, { recursive: true });
  const files = fs
    .readdirSync(srcDir)
    .filter((f) => extOk.has(path.extname(f).toLowerCase()));

  if (files.length === 0) {
    console.log("No .jpg / .png / .webp files in Photos of actual stores.");
    writeEmpty();
    return;
  }

  const items = [];
  files.sort();

  for (let i = 0; i < files.length; i++) {
    const f = files[i];
    const ext = path.extname(f);
    const base = path
      .basename(f, ext)
      .replace(/[^a-zA-Z0-9-_]+/g, "-")
      .replace(/^-+|-+$/g, "")
      .slice(0, 48);
    const safeBase = base || "photo";
    const destName = `store-${String(i + 1).padStart(2, "0")}-${safeBase}${ext.toLowerCase()}`;
    fs.copyFileSync(path.join(srcDir, f), path.join(destDir, destName));
    const captionFromName =
      path
        .basename(f, ext)
        .replace(/[-_]+/g, " ")
        .replace(/\s+/g, " ")
        .trim() || `Cabang ${i + 1}`;
    items.push({ src: `/stores/${destName}`, captionFromName });
  }

  const lines = items.map(
    (it) =>
      `  { src: ${JSON.stringify(it.src)}, caption: { id: ${JSON.stringify(it.captionFromName + " — Bandar Laundry Express")}, en: ${JSON.stringify(it.captionFromName + " — Bandar Laundry Express")} } },`,
  );

  const ts = `/* Generated by scripts/sync-store-photos.cjs — do not edit by hand */
import type { StoreGalleryImage } from "./store-gallery.types";

export const SYNCED_STORE_IMAGES: StoreGalleryImage[] = [
${lines.join("\n")}
];
`;
  fs.writeFileSync(outFile, ts, "utf8");
  console.log(`Synced ${items.length} image(s) → public/stores and lib/store-gallery.synced.ts`);
}

main();
