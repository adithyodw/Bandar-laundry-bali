/**
 * Copies images from `Photos of actual stores/` into `public/store-gallery/`
 * and regenerates `lib/storeGalleryImages.ts`.
 *
 * Run: pnpm run sync-store-photos   (or: npm run sync-store-photos)
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const SRC = path.join(root, "Photos of actual stores");
const DST = path.join(root, "public", "store-gallery");
const OUT = path.join(root, "lib", "storeGalleryImages.ts");

const IMAGE_RE = /\.(jpe?g|png|webp)$/i;

function main() {
  if (!fs.existsSync(SRC)) {
    console.log(
      '[sync-store-photos] Folder "Photos of actual stores" not found — keeping existing gallery data.',
    );
    process.exit(0);
  }

  const files = fs
    .readdirSync(SRC)
    .filter((f) => IMAGE_RE.test(f) && !f.startsWith("."));

  if (files.length === 0) {
    console.log(
      '[sync-store-photos] No images in "Photos of actual stores" — keeping existing gallery data.',
    );
    process.exit(0);
  }

  fs.mkdirSync(DST, { recursive: true });

  for (const name of fs.readdirSync(DST)) {
    if (name === ".gitkeep") continue;
    if (IMAGE_RE.test(name)) {
      fs.unlinkSync(path.join(DST, name));
    }
  }

  files.sort((a, b) => a.localeCompare(b, undefined, { sensitivity: "base" }));

  const items = [];
  files.forEach((file, i) => {
    const ext = path.extname(file).toLowerCase();
    const destName = `store-${String(i + 1).padStart(2, "0")}${ext}`;
    fs.copyFileSync(path.join(SRC, file), path.join(DST, destName));
    items.push({ src: `/store-gallery/${destName}` });
  });

  const ts = `/**
 * Store gallery image paths (served from /public/store-gallery).
 * Regenerated by: pnpm run sync-store-photos
 * Source folder: Photos of actual stores/
 */
export const STORE_GALLERY_ITEMS = ${JSON.stringify(items, null, 2)} as const;
`;

  fs.writeFileSync(OUT, ts, "utf8");
  console.log(`[sync-store-photos] Synced ${items.length} image(s) → public/store-gallery/`);
}

main();
